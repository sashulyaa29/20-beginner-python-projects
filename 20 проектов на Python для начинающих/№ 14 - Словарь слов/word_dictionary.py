# ===============================================================
# Примечание
#
# Изначально планировалось использовать библиотеку PyDictionary
# для получения значений слов онлайн. Однако:
# - сервис работает только с английским языком;
# - сервер часто не отвечает и возвращает пустые данные {}.
#
# Альтернативой была библиотека ruslingua для русского языка,
# но и её сервер периодически возвращал пустые результаты.
#
# Поэтому было принято решение использовать локальный JSON-файл:
# - программа работает офлайн;
# - не зависит от внешних сервисов;
# - словарь можно легко изменять и дополнять вручную.
#
# В проекте были протестированы:
# - онлайн-словари (PyDictionary, ruslingua);
# - локальный словарь в формате JSON.
#
# Используйте тот вариант, который подходит под вашу задачу.
# ===============================================================

import json

with open("dictionary.json", "r", encoding = "utf-8") as f:
    dictionary = json.load(f)

while True:
    word = input("Введите слово (или нажмите Enter для выхода): ")

    if word == "":
        break

    # Ищем слово без учёта регистра
    found = False

    for key in dictionary:
        if word.lower() == key.lower():
            print(key + ":")
            for part_of_speech in dictionary[key]:
                meanings = dictionary[key][part_of_speech]
                print("  " + part_of_speech + ": " + ", ".join(meanings))
            found = True
            break
    
    if not found:
        print("Слово не найдено в словаре.")