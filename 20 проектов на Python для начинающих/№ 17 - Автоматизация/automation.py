from app2 import mobile_number, textbelt_api_key
import requests
import schedule
import time

def send_message():
    resp = requests.post("https://textbelt.com/text", {
        "phone": mobile_number,
        "message": "–î–æ–±—Ä–æ–µ —É—Ç—Ä–æ! –ü—É—Å—Ç—å –∫–æ—Ñ–µ –±—É–¥–µ—Ç –≤–∫—É—Å–Ω—ã–º, –∞ –Ω–∞—Å—Ç—Ä–æ–µ–Ω–∏–µ —É–ª—ë—Ç–Ω—ã–º ‚òïüòÜ.",
        "key": textbelt_api_key
    })

    print(resp.json())

# schedule.every().day.at("06:00").do(send_message)
schedule.every(10).seconds.do(send_message)

while True:
    schedule.run_pending()
    time.sleep(1)